package chapter4operations;

public class IncDec {
    public static void main(String[] args) {
        int a = 1;
        int b = 2;
        int с;
        int d;
        с = ++b;
        d = a++;
        с++;
        System.out.println("a =" + a);
        System.out.println("b =" + b);
        System.out.println("c =" + с);
        System.out.println("d =" + d);


    }
}
